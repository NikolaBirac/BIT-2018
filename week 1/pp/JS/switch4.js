var grade = 1;

switch (grade) {
    case 'A':
        console.log("Good job");
        break;
    case 'B':
        console.log("Pretty good");
        break;
    case 'C':
        console.log("passed");
        break;
    case 'D':
        console.log("Not so good");
        break;
    case 'F':
        console.log("you are a failure");
        break;
    default:
        console.log("Unknown grade");
        break;
}